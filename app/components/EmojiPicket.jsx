const CustomEmojiPicker = ({ onEmojiSelect, onClose }) => {
  const [selectedCategory, setSelectedCategory] = useState('smileys');
  const [searchTerm, setSearchTerm] = useState('');
  const pickerRef = useRef(null);

  useEffect(() => {
    const handleClickOutside = (event) => {
      if (pickerRef.current && !pickerRef.current.contains(event.target)) {
        onClose();
      }
    };

    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, [onClose]);

  const getFilteredEmojis = () => {
    const emojis = EMOJI_DATA[selectedCategory];
    if (!searchTerm) return emojis;
    
    return emojis.filter(emoji => 
      emoji.includes(searchTerm.toLowerCase())
    );
  };

  const handleEmojiClick = (emoji) => {
    onEmojiSelect(emoji);
    onClose();
  };

  return (
    <div ref={pickerRef} className="emoji-picker-container">
      <div className="emoji-picker-header">
        <input
          type="text"
          placeholder="Search emojis..."
          value={searchTerm}
          onChange={(e) => setSearchTerm(e.target.value)}
          className="emoji-search"
        />
      </div>
      
      <div className="emoji-categories">
        {Object.keys(EMOJI_DATA).map(category => (
          <button
            key={category}
            className={`emoji-category ${selectedCategory === category ? 'active' : ''}`}
            onClick={() => setSelectedCategory(category)}
          >
            {category.charAt(0).toUpperCase() + category.slice(1)}
          </button>
        ))}
      </div>

      <div className="emoji-grid">
        {getFilteredEmojis().slice(0, 48).map((emoji, index) => (
          <button
            key={index}
            className="emoji-item"
            onClick={() => handleEmojiClick(emoji)}
            title={emoji}
          >
            {emoji}
          </button>
        ))}
      </div>
    </div>
  );
};

export default CustomEmojiPicker;
